/**
  * @brief This function handles USART1 global interrupt.
  */
void USART1_IRQHandler(void)
{
  /* USER CODE BEGIN USART1_IRQn 0 */
	//прерывание по приходу байта данных, флаг сбрасывается при чтении регистра  
	if (LL_USART_IsActiveFlag_RXNE(USART1)&& LL_USART_IsActiveFlag_RXNE(USART1)) 
	{
		/*************************************DEBUG***************************************/
		/**********************выводим в UART3 сообщение "RXNE" **************************/
		//uint8_t message[]={0x52, 0x58, 0x4e, 0x45, 0x0a};
		//usart_TX(message, sizeof(message));
		/*********************************************************************************/
		
		bposLightReq.buffer[tmpPos]= LL_USART_ReceiveData8(USART1);
		tmpPos++;
		//bposLightReq.position=bposLightReq.position+1; //++ не работает, наверняка как то связано с указателями 0о потом разберусь
	}
	//прерывание по завершению приема пакета данных
	if (LL_USART_IsEnabledIT_IDLE(USART1) && LL_USART_IsActiveFlag_IDLE(USART1)) 
	{	
		//подымаем флаг о получении
		bposLightReq.position=tmpPos;
		tmpPos=0;
		bposLightReq.state=RECIVED;	
		
		
		/*************************************DEBUG***************************************/
		/**********************выводим в UART3 сообщение "IDLE" **************************/
		//uint8_t message[]={0x49, 0x44, 0x4c, 0x45, 0x0a};
		//usart_DEBUG(message, sizeof(message));
		//usart_DEBUG_ASCII(bposLightReq.state);
		/*********************************************************************************/
		
		//сбрасываем флаг			
		LL_USART_ClearFlag_IDLE(USART1);
	}
  /* USER CODE END USART1_IRQn 0 */
  /* USER CODE BEGIN USART1_IRQn 1 */

  /* USER CODE END USART1_IRQn 1 */
}

